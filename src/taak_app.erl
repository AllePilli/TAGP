-module(taak_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

-spec start(_,_) -> {'error',_} | {'ok',pid()}.
start(_StartType, _StartArgs) ->
    taak_sup:start_link().

%%--------------------------------------------------------------------
-spec stop(_) -> 'ok'.
stop(_State) ->
    ok.

